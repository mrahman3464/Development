package com.constructor.thiskeyword;

public class Animal {

	
	public Animal(String hColor, int h, int w) {
		this.hairColor = hColor;
		this.height = h;
		this.weight = w;
		}
		public String hairColor;
		public int height;
		public int weight;
		public void eat() {
		System.out.println("Hi, I am an Animal and I am eating...");
		}
		public void walk() {
		System.out.println("Hi, I am an Animal and I am walking...");
		}
	
	
	
	
	
	
	
}
